import re
import sys


def start():
    # a = input('Введите свои имя, фамилию и e-mail: ')
    a = 'John, Johnson, johnson@gmail.com'
    cutter(a)

def cutter(a):
    if a.count(',') != 0:
        e, f, g = (a.split(','))
    else:
        e, f, g = (a.split(' '))
    namecheck(e.strip(), f.strip())
    mailcheck(g.strip())

def namecheck(a, b):
    print(a, b)
    pattern = '[A-Z][a-z]'  # Я так и не сообразил, что делать в ТакОм СлучАе
    if re.match(pattern, a) == None:
        print("Имя введено некорректно!")
        sys.exit()
    if re.match(pattern, b) == None:
        print("Фамилия введена некорректно!")
        sys.exit()
    print('Имя и фамилия корректны.')

def mailcheck(a):
    pattern = '[a-z0-9\._]+@[a-z0-9]+\.[a-z]+'
    if re.match(pattern, a) == None:
        print("e-mail введен некорректно!")
        sys.exit()
    print("e-mail введен корректно.")


